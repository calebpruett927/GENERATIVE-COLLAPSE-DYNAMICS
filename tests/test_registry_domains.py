# ── Tier-1 identity tests (from registry values) ───────────────────────────


class TestTier1Identities:
    """Verify Tier-1 kernel identities from precomputed registry values.

    These tests validate that the registry itself records conformant values.
    No kernel recomputation needed.
    """

    @pytest.mark.parametrize(
        "domain_key,entity_name,entity",
        _ENTITY_PARAMS,
        ids=lambda p: f"{p[0]}::{p[1]}" if isinstance(p, tuple) else p,
    )
    def test_duality_identity(self, domain_key, entity_name, entity):
        """F + ω = 1 (duality identity, verified from registry)."""
        k = entity["kernel"]
        assert abs(k["F"] + k["omega"] - 1.0) < 1e-12

    @pytest.mark.parametrize(
        "domain_key,entity_name,entity",
        _ENTITY_PARAMS,
        ids=lambda p: f"{p[0]}::{p[1]}" if isinstance(p, tuple) else p,
    )
    def test_integrity_bound(self, domain_key, entity_name, entity):
        """IC ≤ F (integrity bound, verified from registry)."""
        k = entity["kernel"]
        assert k["IC"] <= k["F"] + 1e-12

    @pytest.mark.parametrize(
        "domain_key,entity_name,entity",
        _ENTITY_PARAMS,
        ids=lambda p: f"{p[0]}::{p[1]}" if isinstance(p, tuple) else p,
    )
    def test_log_integrity_relation(self, domain_key, entity_name, entity):
        """IC = exp(κ) (log-integrity relation, verified from registry)."""
        k = entity["kernel"]
        assert abs(k["IC"] - math.exp(k["kappa"])) < 1e-10


