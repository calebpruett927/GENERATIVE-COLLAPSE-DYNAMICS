# ── Live verification (optional, heavyweight) ──────────────────────────────

_LIVE = os.environ.get("UMCP_LIVE_REGISTRY", "0") == "1"


@pytest.mark.skipif(not _LIVE, reason="Set UMCP_LIVE_REGISTRY=1 to run live checks")
class TestLiveVerification:
    """Recompute kernel for every entity and verify registry matches.

    Only runs when UMCP_LIVE_REGISTRY=1 environment variable is set.
    This is the heavyweight check that confirms the registry is current.
    """

    @pytest.mark.parametrize(
        "domain_key,entity_name,entity",
        _ENTITY_PARAMS,
        ids=lambda p: f"{p[0]}::{p[1]}" if isinstance(p, tuple) else p,
    )
    def test_kernel_matches_registry(self, domain_key, entity_name, entity):
        from umcp.frozen_contract import EPSILON
        from umcp.kernel_optimized import compute_kernel_outputs

        c = np.array(entity["trace_vector"])
        c = np.clip(c, EPSILON, 1.0 - EPSILON)
        w = np.ones(len(c)) / len(c)
        result = compute_kernel_outputs(c, w)

        stored = entity["kernel"]
        assert abs(result["F"] - stored["F"]) < 1e-10, f"F drift: {result['F']} vs {stored['F']}"
        assert abs(result["omega"] - stored["omega"]) < 1e-10
        assert abs(result["S"] - stored["S"]) < 1e-10
        assert abs(result["C"] - stored["C"]) < 1e-10
        assert abs(result["kappa"] - stored["kappa"]) < 1e-10
        assert abs(result["IC"] - stored["IC"]) < 1e-10
