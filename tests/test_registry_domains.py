"""Registry-driven domain closure tests.

Replaces 24 individual domain test files (test_258 through test_281) with a
single parametric runner that loads precomputed kernel values from the
centralized test registry (artifacts/test_registry.json).

This provides:
  - One file instead of 24 nearly identical files
  - Tests run against precomputed numbers (no per-entity kernel recomputation)
  - All domain data visible in one JSON for comparison and study
  - Fast verification: registry values vs live kernel at session scope
  - Same test count and coverage as the individual files

The registry is generated by:  python scripts/harvest_test_registry.py

Tier-1 identity checks (F + ω = 1, IC ≤ F, IC = exp(κ)) validate against
precomputed values AND re-verify live if the --live flag or UMCP_LIVE_REGISTRY
env var is set.
"""

from __future__ import annotations

import json
import math
import os
from pathlib import Path
from typing import Any

import numpy as np
import pytest

REGISTRY_PATH = Path(__file__).resolve().parents[1] / "artifacts" / "test_registry.json"


def _load_registry() -> dict[str, Any]:
    """Load the test registry, failing clearly if not found."""
    if not REGISTRY_PATH.exists():
        pytest.skip(
            f"Test registry not found at {REGISTRY_PATH}. Generate it with: python scripts/harvest_test_registry.py"
        )
    with open(REGISTRY_PATH) as f:
        return json.load(f)


# ── Session-scoped registry fixture ─────────────────────────────────────────


@pytest.fixture(scope="session")
def registry() -> dict[str, Any]:
    return _load_registry()


# ── Parametrize helpers ─────────────────────────────────────────────────────


def _domain_keys() -> list[str]:
    """Return domain keys from registry for parametrization."""
    if not REGISTRY_PATH.exists():
        return []
    with open(REGISTRY_PATH) as f:
        reg = json.load(f)
    return list(reg.get("domains", {}).keys())


def _entity_params() -> list[tuple[str, str, dict]]:
    """Return (domain_key, entity_name, entity_data) tuples."""
    if not REGISTRY_PATH.exists():
        return []
    with open(REGISTRY_PATH) as f:
        reg = json.load(f)
    params = []
    for dkey, ddata in reg.get("domains", {}).items():
        for entity in ddata["entities"]:
            params.append((dkey, entity["name"], entity))
    return params


def _theorem_params() -> list[tuple[str, str, dict]]:
    """Return (domain_key, theorem_name, theorem_data) tuples."""
    if not REGISTRY_PATH.exists():
        return []
    with open(REGISTRY_PATH) as f:
        reg = json.load(f)
    params = []
    for dkey, ddata in reg.get("domains", {}).items():
        for theorem in ddata["theorems"]:
            params.append((dkey, theorem["name"], theorem))
    return params


# Pre-compute param lists at collection time
_DOMAIN_KEYS = _domain_keys()
_ENTITY_PARAMS = _entity_params()
_THEOREM_PARAMS = _theorem_params()


# ── Registry structure tests ────────────────────────────────────────────────


class TestRegistryStructure:
    """Validate the registry file itself is well-formed."""

    def test_registry_exists(self):
        assert REGISTRY_PATH.exists(), "Registry not found. Run: python scripts/harvest_test_registry.py"

    def test_registry_has_meta(self, registry):
        assert "_meta" in registry
        meta = registry["_meta"]
        assert meta["n_domains"] == 24
        assert meta["n_entities_total"] == 288  # 24 × 12
        assert meta["n_theorems_total"] == 144  # 24 × 6

    def test_registry_global_summary(self, registry):
        gs = registry["global_summary"]
        assert gs["all_integrity_bounds_satisfied"]
        assert gs["max_duality_residual"] < 1e-12
        assert gs["max_log_integrity_residual"] < 1e-10

    @pytest.mark.parametrize("domain_key", _DOMAIN_KEYS, ids=lambda k: k)
    def test_domain_has_12_entities(self, registry, domain_key):
        assert registry["domains"][domain_key]["n_entities"] == 12

    @pytest.mark.parametrize("domain_key", _DOMAIN_KEYS, ids=lambda k: k)
    def test_domain_has_8_channels(self, registry, domain_key):
        assert registry["domains"][domain_key]["n_channels"] == 8

    @pytest.mark.parametrize("domain_key", _DOMAIN_KEYS, ids=lambda k: k)
    def test_domain_has_4_categories(self, registry, domain_key):
        entities = registry["domains"][domain_key]["entities"]
        cats = {e["category"] for e in entities}
        assert len(cats) == 4

    @pytest.mark.parametrize("domain_key", _DOMAIN_KEYS, ids=lambda k: k)
    def test_domain_has_6_theorems(self, registry, domain_key):
        assert len(registry["domains"][domain_key]["theorems"]) == 6


