# Lookup indices
# ═══════════════════════════════════════════════════════════════════

ELEMENTS_BY_Z: dict[int, Element] = {el.Z: el for el in ELEMENTS}
ELEMENTS_BY_SYMBOL: dict[str, Element] = {el.symbol: el for el in ELEMENTS}
ELEMENTS_BY_NAME: dict[str, Element] = {el.name.lower(): el for el in ELEMENTS}


def get_element(key: int | str) -> Element | None:
    """Look up an element by Z (int), symbol (str), or name (str).

    Returns None if not found.

    >>> get_element(26).name
    'Iron'
    >>> get_element("Fe").Z
    26
    >>> get_element("iron").symbol
    'Fe'
    """
    if isinstance(key, int):
        return ELEMENTS_BY_Z.get(key)
    if isinstance(key, str):
        # Try symbol first (case-sensitive), then name (case-insensitive)
        return ELEMENTS_BY_SYMBOL.get(key) or ELEMENTS_BY_NAME.get(key.lower())
    return None


def element_card(el: Element) -> str:
    """Render a human-readable element info card.

    >>> print(element_card(get_element("Fe")))
    ┌─────────────────────────────────────────┐
    │  26  Fe  Iron                           │
    │  ─────────────────────────────────────  │
    │  Atomic mass:     55.845 amu            │
    │  State (STP):     Solid                 │
    │  Config:          [Ar] 3d6 4s2          │
    │  Oxidation:       +3, +2, +6            │
    │  Electronegativity: 1.83 (Pauling)      │
    │  Atomic radius:   140 pm                │
    │  IE₁:             7.902 eV              │
    │  Electron affin:  0.151 eV              │
    │  Melting point:   1811.0 K              │
    │  Boiling point:   3134.0 K              │
    │  Density:         7.874 g/cm³           │
    └─────────────────────────────────────────┘
    """
    w = 43
    ox = ", ".join(f"{'+' if x > 0 else ''}{x}" for x in el.oxidation_states)
    en = f"{el.electronegativity:.2f} (Pauling)" if el.electronegativity else "n/a"
    ar = f"{el.atomic_radius_pm:.0f} pm" if el.atomic_radius_pm else "n/a"
    ea = f"{el.electron_affinity_eV:.3f} eV" if el.electron_affinity_eV is not None else "n/a"
    mp = f"{el.melting_point_K:.1f} K" if el.melting_point_K is not None else "n/a"
    bp = f"{el.boiling_point_K:.1f} K" if el.boiling_point_K is not None else "n/a"
    dn = f"{el.density_g_cm3:.4g} g/cm³" if el.density_g_cm3 is not None else "n/a"

    lines = [
        f"┌{'─' * (w - 2)}┐",
        f"│  {el.Z:<3d} {el.symbol:<3s} {el.name:<{w - 12}}│",
        f"│  {'─' * (w - 6)}  │",
        f"│  Atomic mass:     {el.atomic_mass:<20} amu  │"[: w - 1] + "│",
        f"│  State (STP):     {el.standard_state:<{w - 22}}│",
        f"│  Config:          {el.electron_config:<{w - 22}}│",
        f"│  Oxidation:       {ox:<{w - 22}}│",
        f"│  Electronegativity: {en:<{w - 24}}│",
        f"│  Atomic radius:   {ar:<{w - 22}}│",
        f"│  IE₁:             {el.ionization_energy_eV:<8.3f} eV{' ' * (w - 28)}│",
        f"│  Electron affin:  {ea:<{w - 22}}│",
        f"│  Melting point:   {mp:<{w - 22}}│",
        f"│  Boiling point:   {bp:<{w - 22}}│",
        f"│  Density:         {dn:<{w - 22}}│",
        f"└{'─' * (w - 2)}┘",
    ]
    return "\n".join(lines)


