"""Comprehensive element database for all 118 elements.

Properties per element (standard reference values):
    - Z: atomic number
    - symbol: IUPAC symbol
    - name: full element name
    - atomic_mass: standard atomic weight (amu / Da)
    - standard_state: phase at STP (Solid, Liquid, Gas, or Expected)
    - electron_config: ground-state electron configuration (abbreviated)
    - oxidation_states: common oxidation states (list of int)
    - electronegativity: Pauling scale (None if not determined)
    - atomic_radius_pm: empirical atomic radius in picometers (None if unknown)
    - ionization_energy_eV: first ionization energy in eV
    - electron_affinity_eV: electron affinity in eV (None if not measured)
    - melting_point_K: melting point in Kelvin (None if unknown)
    - boiling_point_K: boiling point in Kelvin (None if unknown)
    - density_g_cm3: density at STP in g/cm³ (None if unknown)

Sources:
    - NIST Atomic Spectra Database (ionization energies)
    - PubChem / National Library of Medicine (general properties)
    - IUPAC 2021 standard atomic weights
    - CRC Handbook of Chemistry and Physics, 104th ed.
    - WebElements (electronegativity, radii, electron affinity)
    - CODATA 2018 recommended values

Copyright-free reference data: atomic numbers, masses, and physical constants
are factual scientific measurements not subject to copyright.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Any


@dataclass(frozen=True, slots=True)
class Element:
    """Complete element record with standard reference properties."""

    Z: int
    symbol: str
    name: str
    atomic_mass: float  # amu (Da)
    standard_state: str  # "Solid", "Liquid", "Gas", "Expected Solid", etc.
    electron_config: str  # Abbreviated, e.g. "[Ar] 3d6 4s2"
    oxidation_states: tuple[int, ...]  # Common oxidation states
    electronegativity: float | None  # Pauling scale (None = not determined)
    atomic_radius_pm: float | None  # Empirical, pm
    ionization_energy_eV: float  # First IE, eV
    electron_affinity_eV: float | None  # eV (None = not measured / positive)
    melting_point_K: float | None  # K
    boiling_point_K: float | None  # K
    density_g_cm3: float | None  # g/cm³ at STP

    # ── Traditional periodic table labels ────────────────────────
    period: int = 0  # Row 1-7
    group: int | None = None  # Column 1-18 (None for lanthanides/actinides)
    block: str = ""  # "s", "p", "d", "f"
    category: str = ""  # Traditional classification (see CATEGORIES below)

    def to_dict(self) -> dict[str, Any]:
        """Serialize to dict for JSON output."""
        return {
            "Z": self.Z,
            "symbol": self.symbol,
            "name": self.name,
            "atomic_mass": self.atomic_mass,
            "standard_state": self.standard_state,
            "electron_config": self.electron_config,
            "oxidation_states": list(self.oxidation_states),
            "electronegativity": self.electronegativity,
            "atomic_radius_pm": self.atomic_radius_pm,
            "ionization_energy_eV": self.ionization_energy_eV,
            "electron_affinity_eV": self.electron_affinity_eV,
            "melting_point_K": self.melting_point_K,
            "boiling_point_K": self.boiling_point_K,
            "density_g_cm3": self.density_g_cm3,
            "period": self.period,
            "group": self.group,
            "block": self.block,
            "category": self.category,
        }


