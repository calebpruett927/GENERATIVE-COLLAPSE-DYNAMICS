# Validation
# ═══════════════════════════════════════════════════════════════════


def validate_database() -> dict[str, Any]:
    """Run integrity checks on the element database.

    Returns dict with counts and any issues found.
    """
    issues: list[str] = []

    # Check count
    if len(ELEMENTS) != 118:
        issues.append(f"Expected 118 elements, got {len(ELEMENTS)}")

    # Check Z sequence
    for i, el in enumerate(ELEMENTS):
        if i + 1 != el.Z:
            issues.append(f"Element at index {i} has Z={el.Z}, expected {i + 1}")

    # Check no duplicate symbols
    symbols = [el.symbol for el in ELEMENTS]
    if len(set(symbols)) != len(symbols):
        dupes = [s for s in symbols if symbols.count(s) > 1]
        issues.append(f"Duplicate symbols: {set(dupes)}")

    # Check lookup consistency
    for el in ELEMENTS:
        if ELEMENTS_BY_Z[el.Z] is not el:
            issues.append(f"ELEMENTS_BY_Z[{el.Z}] mismatch")
        if ELEMENTS_BY_SYMBOL[el.symbol] is not el:
            issues.append(f"ELEMENTS_BY_SYMBOL[{el.symbol}] mismatch")

    # Property ranges
    for el in ELEMENTS:
        if el.atomic_mass <= 0:
            issues.append(f"{el.symbol}: atomic_mass={el.atomic_mass} <= 0")
        if el.ionization_energy_eV <= 0:
            issues.append(f"{el.symbol}: IE={el.ionization_energy_eV} <= 0")
        if el.electronegativity is not None and not 0.5 <= el.electronegativity <= 4.1:
            issues.append(f"{el.symbol}: EN={el.electronegativity} out of [0.5, 4.1]")

    return {
        "count": len(ELEMENTS),
        "unique_symbols": len(set(symbols)),
        "issues": issues,
        "valid": len(issues) == 0,
    }


if __name__ == "__main__":
    # Self-test
    result = validate_database()
    print(f"Database: {result['count']} elements, {result['unique_symbols']} unique symbols")
    if result["valid"]:
        print("✓ All integrity checks passed")
    else:
        print(f"✗ {len(result['issues'])} issues:")
        for issue in result["issues"]:
            print(f"  - {issue}")

    # Show a few example cards
    for sym in ("H", "Fe", "Au", "Og"):
        el = get_element(sym)
        if el:
            print()
            print(element_card(el))
