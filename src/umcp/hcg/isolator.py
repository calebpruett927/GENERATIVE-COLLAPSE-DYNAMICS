def _resolve_site_url(config: DomainConfig) -> tuple[str, str]:
    """Determine site URL and base path for an isolated domain.

    Returns (site_url, base_path).
    """
    if config.base_url:
        return config.base_url, "/"

    # Default: subpath on GitHub Pages for monorepo, or root for standalone
    if config.deploy_target == "vercel" or config.deploy_target == "netlify":
        return f"https://{config.slug}.gcd-kernel.org", "/"
    else:
        # Standalone GitHub Pages repo
        return "https://calebpruett927.github.io", f"/gcd-{config.slug}/"


def isolate_domain(
    domain: str,
    output_dir: Path | str | None = None,
    *,
    build_content: bool = True,
) -> IsolatedSite:
    """Create a standalone Astro project for one domain.

    Parameters
    ----------
    domain : str
        Domain slug (e.g. "finance").
    output_dir : Path, optional
        Where to create the project. Defaults to deploy/<domain>.
    build_content : bool
        If True, run the builder to populate content. If False, only
        scaffold the project structure (useful for CI where content
        is provided as an artifact).

    Returns
    -------
    IsolatedSite
        Metadata about the created project.
    """
    config = get_domain_config(domain)

    if output_dir is None:
        from umcp.hcg.extractor import _repo_root

        output_dir = _repo_root() / "deploy" / domain
    else:
        output_dir = Path(output_dir)

    project_dir = output_dir
    project_dir.mkdir(parents=True, exist_ok=True)

    site_url, base_path = _resolve_site_url(config)

    # --- Scaffold project structure ---
    src_dir = project_dir / "src"
    content_dir = src_dir / "content" / config.slug
    layouts_dir = src_dir / "layouts"
    pages_dir = src_dir / "pages"

    for d in [content_dir, layouts_dir, pages_dir]:
        d.mkdir(parents=True, exist_ok=True)

    # astro.config.mjs
    astro_config = _ASTRO_CONFIG_TEMPLATE.format(
        display_name=config.display_name,
        site_url=site_url,
        base_path=base_path,
    )
    (project_dir / "astro.config.mjs").write_text(astro_config, encoding="utf-8")

    # package.json
    pkg = dict(_PACKAGE_JSON_TEMPLATE)
    pkg["name"] = f"gcd-{config.slug}"
    (project_dir / "package.json").write_text(json.dumps(pkg, indent=2), encoding="utf-8")

    # content.config.ts
    content_config = _CONTENT_CONFIG_TEMPLATE.format(
        display_name=config.display_name,
        slug=config.slug,
    )
    (src_dir / "content.config.ts").write_text(content_config, encoding="utf-8")

    # tailwind.config.mjs
    (project_dir / "tailwind.config.mjs").write_text(_TAILWIND_CONFIG, encoding="utf-8")

    # Base layout
    layout = _BASE_LAYOUT_TEMPLATE.format(
        display_name=config.display_name,
        tagline=config.tagline,
        primary_color=config.primary_color,
        accent_color=config.accent_color,
    )
    (layouts_dir / "BaseLayout.astro").write_text(layout, encoding="utf-8")

    # Pages
    (pages_dir / "index.astro").write_text(_INDEX_PAGE, encoding="utf-8")

    for page_name, page_title in [
        ("contract", f"Contract — {config.display_name}"),
        ("theorems", f"Theorems — {config.display_name}"),
        ("entities", f"Entities — {config.display_name}"),
    ]:
        page = _SUBPAGE_TEMPLATE.format(
            page_name=page_name,
            page_title=page_title,
        )
        (pages_dir / f"{page_name}.astro").write_text(page, encoding="utf-8")

    # --- Build content ---
    pages_built = 0
    if build_content:
        build_site(domain=domain, output_dir=src_dir / "content")
        # Count generated files
        pages_built = sum(1 for _ in content_dir.rglob("*.md"))

    logger.info(
        "Isolated domain %s → %s (%d pages)",
        domain,
        project_dir,
        pages_built,
    )

    return IsolatedSite(
        domain=domain,
        project_dir=project_dir,
        content_dir=content_dir,
        config=config,
        pages_built=pages_built,
        ready=pages_built > 0,
    )


def isolate_all(
    output_dir: Path | str | None = None,
) -> list[IsolatedSite]:
    """Create standalone projects for all 20 domains.

    Returns list of IsolatedSite objects.
    """
    from umcp.hcg.domain_config import list_domains

    if output_dir is None:
        from umcp.hcg.extractor import _repo_root

        output_dir = _repo_root() / "deploy"
    else:
        output_dir = Path(output_dir)

    results: list[IsolatedSite] = []
    for domain in list_domains():
        try:
            site = isolate_domain(domain, output_dir=output_dir / domain)
            results.append(site)
        except Exception as exc:
            logger.error("Failed to isolate domain %s: %s", domain, exc)

    logger.info("Isolated %d domains → %s", len(results), output_dir)
    return results


