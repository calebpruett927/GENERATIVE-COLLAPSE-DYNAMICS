def main(argv: list[str] | None = None) -> int:
    """CLI for the domain isolator."""
    import argparse

    parser = argparse.ArgumentParser(
        prog="hcg-isolator",
        description="HCG Domain Isolator — Create standalone domain sites",
    )
    parser.add_argument(
        "--domain",
        "-d",
        help="Isolate a single domain (e.g. 'finance')",
    )
    parser.add_argument(
        "--all",
        "-a",
        action="store_true",
        dest="isolate_all",
        help="Isolate all 20 domains",
    )
    parser.add_argument(
        "--output",
        "-o",
        default=None,
        help="Output directory (default: deploy/<domain>)",
    )
    parser.add_argument(
        "--no-content",
        action="store_true",
        help="Scaffold only — don't generate content",
    )
    parser.add_argument(
        "--verbose",
        "-v",
        action="store_true",
        help="Enable verbose logging",
    )

    args = parser.parse_args(argv)
    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(levelname)s: %(message)s",
    )

    output = Path(args.output) if args.output else None

    if args.isolate_all:
        sites = isolate_all(output_dir=output)
        print(f"\nIsolated {len(sites)} domain sites.")
        for s in sites:
            status = "ready" if s.ready else "scaffold only"
            print(f"  {s.domain:30s} {s.pages_built:3d} pages  ({status})")
        return 0

    if args.domain:
        site = isolate_domain(
            args.domain,
            output_dir=output,
            build_content=not args.no_content,
        )
        status = "ready" if site.ready else "scaffold only"
        print(f"Isolated {site.domain} → {site.project_dir} ({site.pages_built} pages, {status})")
        return 0

    print("Specify --domain <name> or --all")
    return 1


if __name__ == "__main__":
    import sys

    sys.exit(main())
