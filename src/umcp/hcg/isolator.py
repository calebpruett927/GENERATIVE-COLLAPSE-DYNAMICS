"""
Domain Isolator — Produces standalone, hermetic sites for individual domains.

The current Astro build produces one combined site with all 20 domains.
The isolator creates an independent, deployable site for a single domain
that can be pushed to its own repo, Vercel project, or Netlify site.

Architecture:
    isolator reads DomainConfig + SiteData
    → generates a self-contained Astro project (config, package.json, layout, pages)
    → builder populates content into that project
    → npm build produces a hermetic static site

This is the structural mechanism for "a fleet of independent, autonomous websites."
The content is identical to the monolith build; only the packaging changes.

Usage:
    from umcp.hcg.isolator import isolate_domain, IsolatedSite

    site = isolate_domain("finance", output_dir="deploy/finance")
    # site.project_dir → deploy/finance/  (ready for `npm install && npm run build`)

    # Or from CLI:
    python -m umcp.hcg.isolator --domain finance --output deploy/finance
"""

from __future__ import annotations

import json
import logging
from dataclasses import dataclass
from pathlib import Path

from umcp.hcg.builder import build_site
from umcp.hcg.domain_config import DomainConfig, get_domain_config

logger = logging.getLogger(__name__)


@dataclass
class IsolatedSite:
    """Result of isolating a domain into a standalone project."""

    domain: str
    project_dir: Path
    content_dir: Path
    config: DomainConfig
    pages_built: int
    ready: bool  # True if npm install + build would succeed


